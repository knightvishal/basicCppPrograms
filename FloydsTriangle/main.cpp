#include <iostream>

using namespace std;

void triangleFunc()
{
    int n, i, j, k = 1;
    cout << "Enter number of rows to print\n\n";
    cin >> n;

    for(i = 1; i <= n; i++)
    {
        for(j = 1; j <= i; ++j)
        {
            cout << k << " ";
            ++k;
        }
        cout << endl;
    }
}

int main()
{
    triangleFunc();
    return 0;
}
